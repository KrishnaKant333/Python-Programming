# 1. Artihmetic Operator : +, -, *, / etc
# 2. Assignment Operator : =, +=, *=, etc
# 3. Comparison Operator : ==, >, <=, != etc
# 4. Logical Operator : and, or, not
a=4; b=5
c=a+b
print(c)
d=7
d+=3
print(d)
# comparison operators have return type boolean i.e. either true or false
e = 5<4
print(e) # --> returns false
f = True or False # OR Gate
print(f)
g = True and False # AND Gate
print(g)
h = not True # NOT Gate
# h = not(True) # is the same
print(h)
