str1 = "Is kks a..\tgood boy?!" # \t --> tab
str2 = "Kks is a good boy..\nbut a little naughty boy" # \n --> new line
print(str1)
print(str2)

# to escape characters like "" or '' or \ we can use \
str3 = "Kks is a so called \"Good\" boy"
print(str3)
str4 = "Is that so\\n"
print(str4)
# to escape even the escape sequence characters we can use the \
